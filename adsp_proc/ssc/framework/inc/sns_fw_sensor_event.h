/**
 * @note Unlike the other data structures in this directory, this structure
 * cannot be freely casted to/from sns_sensor_event.
 */
typedef struct sns_fw_sensor_event
{
  struct
  {
#ifdef SNS_DEBUG_EVENT_SERVICE
    uint32_t headcode;
    intptr_t caller;
#endif
    /* Remaining clients to process this event.
     * Set to -1 after sns_event_service_api::alloc_event. */
    int32_t client_cnt;
    uint32_t fw_event_len;
  } header;
  sns_sensor_event event;
} sns_fw_sensor_event;
